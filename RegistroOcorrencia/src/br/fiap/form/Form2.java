    //Adquire os dados do laboratorio, valida e cria um objeto Laboratorio
    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        int codigo = 0;
        boolean conseguiu = false;
        codigo = Integer.parseInt(txtCodigo.getText());
        String lab = txtLab.getText();

        if (codigo == 0 || lab.equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos!!");
        } else {
            Laboratorio laboratorio = new Laboratorio(codigo, lab);

            DAO dao = new DAO();
            conseguiu = dao.inserirLaboratorio(laboratorio);
            if (conseguiu) {
                JOptionPane.showMessageDialog(this, "A ocorrencia foi registrada no banco!");
            }

        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

